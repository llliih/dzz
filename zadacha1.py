num = int(input("Введите число"))   #Дано число.
if num < 7:
   print("Yes")                        #Если оно меньше 7, то вывести Yes,
elif num > 10:
   print("No")                         #если больше 10, то вывести No,\
elif num == 9:
   print ("Error")                     #если равно 9, то вывести Error.